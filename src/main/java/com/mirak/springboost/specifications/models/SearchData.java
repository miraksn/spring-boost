package com.mirak.springboost.specifications.models;

public class SearchData {

	private int page;
	private int size;
	private String sort;
	private String order;

	private SearchModel SearchData;

	/**
	 * @return the page
	 */
	public int getPage() {
		return page;
	}

	/**
	 * @param page the page to set
	 */
	public void setPage(int page) {
		this.page = page;
	}

	/**
	 * @return the size
	 */
	public int getSize() {
		return size;
	}

	/**
	 * @param size the size to set
	 */
	public void setSize(int size) {
		this.size = size;
	}

	/**
	 * @return the sort
	 */
	public String getSort() {
		return sort;
	}

	/**
	 * @param sort the sort to set
	 */
	public void setSort(String sort) {
		this.sort = sort;
	}

	/**
	 * @return the order
	 */
	public String getOrder() {
		return order;
	}

	/**
	 * @param order the order to set
	 */
	public void setOrder(String order) {
		this.order = order;
	}

	/**
	 * @return the searchData
	 */
	public SearchModel getSearchData() {
		return SearchData;
	}

	/**
	 * @param searchData the searchData to set
	 */
	public void setSearchData(SearchModel searchData) {
		SearchData = searchData;
	}
}
